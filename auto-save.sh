    # –ü–æ–ø—ã—Ç–∫–∞ –ø—É—à–∞ (–µ—Å–ª–∏ –µ—Å—Ç—å –∏–Ω—Ç–µ—Ä–Ω–µ—Ç)
    if git push origin main >> "$LOG_FILE" 2>&1; then
