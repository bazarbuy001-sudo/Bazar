async function seed() {
  console.log('üå± Seeding categories...');

  try {
    // Clear existing data
    await prisma.subCategory.deleteMany({});
    await prisma.category.deleteMany({});

    // Seed fabric categories
    for (const [categoryName, subcategories] of Object.entries(fabricCategories)) {
      const category = await prisma.category.create({
        data: {
          name: categoryName,
          type: 'fabric',
          description: `–ö–∞—Ç–µ–≥–æ—Ä–∏—è —Ç–∫–∞–Ω–µ–π: ${categoryName}`
        }
      });

      for (const subcat of subcategories) {
        await prisma.subCategory.create({
          data: {
            categoryId: category.id,
            name: subcat.replace(/_/g, ' ')
          }
        });
      }

      console.log(`‚úÖ –°–æ–∑–¥–∞–Ω—ã ${subcategories.length} –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏–π –¥–ª—è "${categoryName}"`);
    }

    // Seed accessory categories
    for (const [categoryName, subcategories] of Object.entries(accessoryCategories)) {
      const category = await prisma.category.create({
        data: {
          name: categoryName,
          type: 'accessory',
          description: `–ö–∞—Ç–µ–≥–æ—Ä–∏—è —Ñ—É—Ä–Ω–∏—Ç—É—Ä—ã: ${categoryName}`
        }
      });

      for (const subcat of subcategories) {
        await prisma.subCategory.create({
          data: {
            categoryId: category.id,
            name: subcat.replace(/_/g, ' ')
          }
        });
      }

      console.log(`‚úÖ –°–æ–∑–¥–∞–Ω—ã ${subcategories.length} –ø–æ–¥–∫–∞—Ç–µ–≥–æ—Ä–∏–π –¥–ª—è "${categoryName}"`);
    }

    console.log('‚ú® Seeding completed!');
  } catch (error) {
    console.error('‚ùå Seeding failed:', error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

